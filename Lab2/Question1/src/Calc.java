public class Calc{
	public static double Add(double a, double b) {
		double c;
		c = a + b;
		return c;
	}
	public static double Sub(double a, double b) {
		double c;
		c = a - b;
		return c;
	}
	public static double Mult(double a, double b) {
		double c;
		c = a * b;
		return c;
	}
	public static double Div(double a, double b) {
		double c;
		c = a / b;
		return c;
	}
}